package aula_02_variaveis;

public class OperadoresUnarios {

		public static void main(String[] args) {
		
			int contador = 1000;
			
			System.out.println("Valor inicial de contador: " + contador);
			
			//System.out.println("Valor Pré Inrementado: " + (++ contador));
			
			System.out.println("Valor Pós Inrementado: " + (contador ++));
			
			System.out.println("Novo valor de contador: " + + contador);
		}
}

